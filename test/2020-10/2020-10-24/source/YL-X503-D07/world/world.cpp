inline int power(int a, int b, int p) { int r = 1; while ( b ) { if ( b & 1 ) r = r * a % p; a = a * a % p; b >>= 1; } return r; }

inline bool Check(int n)
{
    if ( vis[n + 1] ) return false;

    const int p = n + 1, N = n; 
    for ( int i = 2; i * i <= n; ++ i ) 
    {
        if ( n % i != 0 ) continue ;
        while ( n % i == 0 ) n /= i;
        if ( power(2, N / i, p) == 1 ) return false;
    }
    if ( n > 1 && power(2, N / n, p) == 1 ) return false;

    return true;
}

